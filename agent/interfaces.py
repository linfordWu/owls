# ---------------------------------------------------------------------------
# Experience (Agent self-evolution)
# ---------------------------------------------------------------------------

class Experience(TypedDict, total=False):
    """One recorded task execution for the experience store."""

    experience_id: str
    session_id: str
    input_msg: str
    plan_summary: str
    tool_sequence: list[str]
    execution_time_ms: int
    failure_reason: str | None
    user_feedback: str | None
    success: bool
    timestamp: str


class ReflectionReport(TypedDict, total=False):
    """Output of the reflection engine."""

    reflection_id: str
    experience_id: str
    insights: list[str]
    suggested_improvements: list[str]
    prompt_fragment: str | None
    timestamp: str


class PromptFragment(TypedDict, total=False):
    """A distilled prompt template fragment from consolidated experiences."""

    fragment_id: str
    hash: str
    content: str
    source_experience_ids: list[str]
    created_at: str
