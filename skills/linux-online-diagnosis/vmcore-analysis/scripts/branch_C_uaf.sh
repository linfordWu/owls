#!/usr/bin/env bash
# =============================================================================
# 脚本：branch_C_uaf.sh
# 用途：Use-After-Free 故障完整分析（源码主导 + 纯vmcore 双路径）
# 使用：bash branch_C_uaf.sh <vmcore_path> <vmlinux_path> [src_dir]
# =============================================================================

set -euo pipefail

VMCORE="${1:-/var/crash/vmcore}"
VMLINUX="${2:-/usr/lib/debug/lib/modules/$(uname -r)/vmlinux}"
SRC_DIR="${3:-}"
CRASH_CMD="crash -s ${VMLINUX} ${VMCORE}"

if [[ "${1:-}" == "--help" ]] || [[ "${1:-}" == "-h" ]]; then
  echo "用途：Use-After-Free (UAF) 完整分析"
  echo "使用：bash $0 <vmcore> <vmlinux> [src_dir]"
  exit 0
fi

HAS_SRC=false
[[ -n "${SRC_DIR}" && -d "${SRC_DIR}" ]] && HAS_SRC=true

echo "=================================================================="
echo " 分支C：Use-After-Free 分析"
echo " 分析路径：$( $HAS_SRC && echo '【源码主导】' || echo '【纯vmcore】' )"
echo "=================================================================="

# --------------------------------------------------------------------------
# C1. KASAN 报告（案发现场全貌）
# --------------------------------------------------------------------------
echo ""
echo "【C1】KASAN UAF 完整报告"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
log | grep -B5 -A50 "use-after-free\|KASAN.*free"
EOF

# --------------------------------------------------------------------------
# C2. 崩溃调用栈（访问路径 = 案发时刻）
# --------------------------------------------------------------------------
echo ""
echo "【C2】崩溃调用栈（USE 路径）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
bt -f
EOF

# --------------------------------------------------------------------------
# C3. 源码-汇编对齐（Step 2）
# --------------------------------------------------------------------------
echo ""
echo "【C3】崩溃点反汇编（定位访问指令）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
dis -l $(bt | grep "^#0" | awk '{print $NF}') 2>/dev/null | head -40 \
  || echo "请手动执行: dis -l <崩溃函数名>"
EOF

# --------------------------------------------------------------------------
# C4. 目标对象内存状态（确认 UAF 物证）
# --------------------------------------------------------------------------
echo ""
echo "【C4】目标对象内存状态（poison 值确认）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
# KASAN 报告中的被访问地址
# 0x6b6b6b6b = SLAB_POISON（已释放对象的填充值）
echo "请从 C1 的 KASAN 报告提取被访问地址，执行："
echo "  rd <uaf_addr> 16          # 看是否含 0x6b6b6b6b poison 值"
echo "  kmem <uaf_addr>           # 确认对象已标记为 SLAB_FREE"
echo "  kmem -S <slab_cache_name> # 查看该 slab cache 详情"
EOF

echo ""
echo "【C4.1】Slab 缓存状态（识别对象类型）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
kmem -s | grep -E "task_struct|file|socket|skb|inode|dentry|mm_struct|cred"
EOF

# --------------------------------------------------------------------------
# C5. KASAN 的分配/释放 backtrace
# --------------------------------------------------------------------------
echo ""
echo "【C5】KASAN 分配/释放路径（FREE 路径起点）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
log | grep -B2 -A25 "freed by\|Freed by\|allocated by\|Allocated by"
EOF

# --------------------------------------------------------------------------
# C6. 逐帧追踪（Step 3）：USE 路径的根因帧
# --------------------------------------------------------------------------
echo ""
echo "【C6】USE 路径逐帧分析（找到谁还持有已释放对象的引用）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
bt -l
EOF

if $HAS_SRC; then
  echo ""
  echo "【C6.1】[源码路径] USE 路径逐帧追踪指引"
  echo "------------------------------------------------------------------"
  cat << 'SRCGUIDE'
在源码中追踪"谁还持有已释放对象的引用"：

Step A：确认对象类型（从 kmem -S 输出的 slab cache 名称）
  → 对象类型决定了去哪里找引用计数管理代码

Step B：追踪引用计数操作（数据流 Step 4）
  grep -rn "<type>_get\|<type>_put\|get_<type>\|put_<type>" ${SRC_DIR}/
  grep -rn "refcount_inc\|refcount_dec\|atomic_inc\|atomic_dec" ${SRC_DIR}/path/

Step C：逐条代码路径配对 get/put
  正常路径：get() .... put()   → 是否平衡？
  错误路径：是否有额外的 put() 导致提前释放？
  中断路径：是否有不对称的 put()？

Step D：找到"使用"时的代码路径
  在调用栈对应的函数中，确认通过什么数据结构持有对该对象的引用
  检查该持有路径是否在 put() 之后仍被保留

常见 UAF 根因代码特征：
  □ 释放后未置 NULL：kfree(ptr); /* 漏了 ptr = NULL */ use(ptr);
  □ 引用计数提前归零：错误路径多做了一次 put()
  □ RCU 保护对象在读临界区外访问：rcu_read_unlock(); p->field++; ← UAF
  □ 并发释放无锁保护：线程A释放，线程B同时访问
SRCGUIDE
else
  echo ""
  echo "【C6.1】[vmcore路径] 持有引用的数据结构检查"
  echo "------------------------------------------------------------------"
  ${CRASH_CMD} --no_scroll << 'EOF'
# 检查调用栈上层函数对应的数据结构
# 找到哪个结构体字段仍指向已释放的对象
bt -f | head -80
EOF
fi

# --------------------------------------------------------------------------
# C7. FREE 路径追踪（数据流 Step 4：找到释放时机）
# --------------------------------------------------------------------------
echo ""
echo "【C7】FREE 路径分析（释放时机）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
# 从调用栈和 KASAN freed-by 信息，定位释放函数
bt | grep -E "kfree|kmem_cache_free|free|put_|release|destroy|_put\b"
log | grep -A15 "freed by" | head -20
EOF

if $HAS_SRC; then
  echo ""
  echo "【C7.1】[源码路径] 释放路径源码定位"
  echo "------------------------------------------------------------------"
  cat << 'SRCGUIDE'
根据 KASAN "freed by" backtrace，找到释放操作的源码位置：

  1. 定位释放函数（如 kfree、kmem_cache_free）调用点
  2. 向上追溯：谁调用了释放函数？触发条件是什么？
  3. 检查释放后是否有 ptr = NULL：
     grep -n -A2 "kfree\|kmem_cache_free\|_put\b\|release" ${SRC_DIR}/path/file.c
  4. 对比 USE 路径的调用时机，分析两者之间的时序关系

如果是引用计数问题，执行：
  grep -n "refcount\|atomic" ${SRC_DIR}/path/file.c
  → 找到所有 inc/dec 操作，验证是否对称
SRCGUIDE
fi

# --------------------------------------------------------------------------
# C8. 并发竞态窗口分析
# --------------------------------------------------------------------------
echo ""
echo "【C8】并发竞态窗口分析"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
# 查看所有 CPU 的调用栈，判断是否有并发场景
bt -a | grep -E "CPU|#0|#1|#2" | head -40
EOF

# --------------------------------------------------------------------------
# C9. 反事实验证（Step 5）
# --------------------------------------------------------------------------
echo ""
echo "=================================================================="
echo " Step 5：反事实验证清单"
echo "=================================================================="
cat << 'VERIFY'

【验证1】生命周期自洽性
  假设根因为：<具体引用计数缺陷/提前释放/无锁竞态>

  对于引用计数问题：
    - 配对后的 get/put 是否平衡？（遍历所有调用路径）
    - 归零时机（对象释放）是否在所有引用消失之后？

  对于无锁竞态：
    - 识别竞态窗口（A释放 与 B访问 之间的时间窗口）
    - 加锁后是否能消除此窗口？

【验证2】正向推演
  ① 触发条件：<什么场景/并发状态下触发？>
  ② 释放路径执行：<对象在哪里被释放？>
  ③ 访问路径执行：<对象在哪里被访问（已释放）？>
  ④ 崩溃点：<触发 page fault/KASAN 告警的位置>
  对比 vmcore：推演路径 == 实际 bt 路径？  □ 是  □ 否

【验证3】修复有效性
  - 若增加引用计数保护：持有引用期间对象不被释放 → UAF 消除？ □
  - 若释放后置 NULL：后续 NULL 检查拦截访问 → UAF 消除？ □
  - 若加锁保护：释放与访问互斥 → UAF 消除？ □

VERIFY

echo ""
echo "=================================================================="
echo " 分析结论模板"
echo "=================================================================="
cat << 'TEMPLATE'

## 崩溃概要
  故障模式：Use-After-Free
  置信度：<高/中/低>
  分析路径：<源码主导 / 纯vmcore>

## 完整因果链
  [<触发场景>] → [<对象> 在 <位置> 被提前释放]
  → [<持有引用的代码> 仍持有指向已释放内存的指针]
  → [访问 <字段>] → [KASAN UAF 告警 / Page Fault → Panic]

## 根因结论
  对象类型：<从 slab cache 推断>
  FREE 路径：<file>:<line>  函数：<func>
  USE 路径：<file>:<line>   函数：<func>
  根因类型：<引用计数不对称 / 释放后未置NULL / 无锁并发竞态>
  具体缺陷：<描述>

