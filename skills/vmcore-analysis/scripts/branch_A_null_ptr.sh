#!/usr/bin/env bash
# =============================================================================
# 脚本：branch_A_null_ptr.sh
# 用途：空指针解引用故障 —— vmcore 轨道 + 源码轨道并行分析
# 使用：bash branch_A_null_ptr.sh <vmcore_path> <vmlinux_path> [src_dir]
# 参数：
#   $1  vmcore 路径（默认 /var/crash/vmcore）
#   $2  vmlinux 路径（默认系统路径）
#   $3  源码目录（可选；有无均执行，有时额外输出源码追踪指引）
# 说明：
#   vmcore 轨道和源码轨道同时进行，输出各自结论，最终供交叉验证
# =============================================================================

set -euo pipefail

VMCORE="${1:-/var/crash/vmcore}"
VMLINUX="${2:-/usr/lib/debug/lib/modules/$(uname -r)/vmlinux}"
SRC_DIR="${3:-}"
CRASH_CMD="crash -s ${VMLINUX} ${VMCORE}"

if [[ "${1:-}" == "--help" ]] || [[ "${1:-}" == "-h" ]]; then
  echo "用途：空指针解引用（NULL pointer dereference）双轨并行分析"
  echo "使用：bash $0 <vmcore> <vmlinux> [src_dir]"
  echo "  src_dir: 可选，内核/驱动源码根目录"
  echo "           无论是否提供，vmcore 轨道均会完整执行"
  echo "           提供时，额外输出源码轨道的追踪指引"
  exit 0
fi

HAS_SRC=false
[[ -n "${SRC_DIR}" && -d "${SRC_DIR}" ]] && HAS_SRC=true

echo "=================================================================="
echo " 分支A：空指针解引用 —— 双轨并行分析"
echo " vmcore 轨道：始终执行（逆向推理）"
echo " 源码轨道：$( $HAS_SRC && echo "已启用（${SRC_DIR}）" || echo '未提供源码，仅 vmcore 轨道' )"
echo "=================================================================="

echo ""
echo "=================================================================="
echo " ▶ vmcore 轨道（逆向推理）— Step V1~V4"
echo "=================================================================="

# --------------------------------------------------------------------------
# A1. 确认 panic 类型
# --------------------------------------------------------------------------
echo ""
echo "【A1】确认 panic 类型与崩溃地址"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
log | grep -E "NULL pointer|unable to handle|BUG: unable|Oops" | head -5
EOF

# --------------------------------------------------------------------------
# A2. 崩溃现场：调用栈 + 寄存器（Step 1 输出）
# --------------------------------------------------------------------------
echo ""
echo "【A2】崩溃现场（完整调用栈 + 寄存器）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
bt -f
EOF

# 保存 bt 输出供后续脚本处理
${CRASH_CMD} --no_scroll << 'EOF' > /tmp/vmcore_bt.txt 2>/dev/null
bt -f
EOF

# --------------------------------------------------------------------------
# A3. 源码-汇编对齐（Step 2 —— 有源码时的关键步骤）
# --------------------------------------------------------------------------
echo ""
echo "【A3】崩溃点反汇编（源码行号映射）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
# dis -l 输出带源码行号的反汇编，是源码-汇编对齐的关键
# 格式：0xaddr <func+offset>: instruction    /path/to/file.c:line_no
bt | grep "^#0" | awk '{print $NF}' | xargs -I{} sh -c 'echo "dis -l {}" | crash '"${VMLINUX}"' '"${VMCORE}"' 2>/dev/null | head -40' 2>/dev/null \
  || echo "请手动执行: dis -l <崩溃函数名>  获取源码行号映射"
EOF

echo ""
echo "【A3.1】RIP 地址的精确汇编上下文"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
# 显示崩溃时 RIP 前后的汇编，重点找 mov/lea 操作目标为 NULL 的指令
bt | grep "RIP\|rip" | head -3
EOF

if $HAS_SRC; then
  echo ""
  echo "【A3.2】[源码路径] 版本验证"
  echo "------------------------------------------------------------------"
  echo "请执行以下验证（确保源码与 vmcore 版本一致）："
  echo "  1. 对比 dis -l 输出的行号，与 ${SRC_DIR} 中对应文件的实际内容是否匹配"
  echo "  2. 若行号对应代码逻辑不符，说明版本不匹配，需退回纯vmcore分析"
fi

# --------------------------------------------------------------------------
# A4. 找到值为 NULL 的寄存器（Step 1 数据提取）
# --------------------------------------------------------------------------
echo ""
echo "【A4】寄存器值分析（定位 NULL 寄存器）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
# 打印所有通用寄存器，找值为 0x0 的寄存器
# 结合 A3 的反汇编，确认哪个寄存器持有了 NULL 指针
bt -f | grep -E "RIP|RSP|RAX|RBX|RCX|RDX|RSI|RDI|R8 |R9 |R10|R11|R12|R13|R14|R15|RBP" | head -20
EOF

# --------------------------------------------------------------------------
# A5. 调用帧逐帧追踪（Step 3 核心）
# --------------------------------------------------------------------------
echo ""
echo "【A5】调用帧逐帧分析（追溯根因帧）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
bt -l
EOF

# --------------------------------------------------------------------------
# A6. 数据流溯源（V 轨道 Step V3：数据状态验证）
# --------------------------------------------------------------------------
echo ""
echo "【A6】NULL 值数据流追踪（vmcore 轨道 Step V3）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
echo "请根据 A4/A5 的结果执行以下命令之一："
echo "  struct <object_type> <object_addr>    # 检查容器对象，看哪个字段为 NULL"
echo "  kmem <object_addr>                    # 确认对象是否已被释放"
echo "  rd <object_addr> 16                   # 直接读取内存内容"
EOF

# --------------------------------------------------------------------------
# A7. 内核日志上下文（时序验证）
# --------------------------------------------------------------------------
echo ""
echo "【A7】崩溃前日志（时序分析，排除硬件因素）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
log | grep -B30 "NULL pointer\|Oops\|BUG:" | tail -40
EOF

# --------------------------------------------------------------------------
# vmcore 轨道结论模板
# --------------------------------------------------------------------------
echo ""
echo "=================================================================="
echo " ▶ vmcore 轨道结论（填写后供交叉验证使用）"
echo "=================================================================="
cat << 'VMCORE_CONCLUSION'

  崩溃位置：RIP=<addr>  函数：<func+offset>  源码行：<dis -l 结果>
  调用链：  #0 → #1 → #2 → ... → #N
  NULL 寄存器：<REG>=0x0
  容器对象：<struct 类型> @ <addr>，字段 <field> = NULL
  vmcore 侧根因假设：<基于以上数据的推断，例如：
    "dev->buf 在 A2 步的调用栈中为 NULL，其初始化路径在
    某错误返回分支中未执行">

VMCORE_CONCLUSION

# ==========================================================================
# ▶ 源码轨道（正向追踪）— Step S1~S5
# ==========================================================================
echo ""
echo "=================================================================="
echo " ▶ 源码轨道（正向追踪）— Step S1~S5"
if $HAS_SRC; then
  echo "   源码目录：${SRC_DIR}"
else
  echo "   [未提供源码，以下为源码分析方法指引，获取源码后执行]"
fi
echo "=================================================================="

echo ""
echo "【Step S1】以 vmcore 崩溃帧为入口"
echo "------------------------------------------------------------------"
echo "取 vmcore 轨道输出，记录以下信息作为源码分析起点："
echo "  崩溃函数：<来自 A2 bt #0>"
echo "  RIP 地址：<来自 A2>"
echo "  NULL 寄存器：<来自 A4>"
echo "  调用链：<来自 A5 bt -l>"

echo ""
echo "【Step S2】源码-汇编对齐"
echo "------------------------------------------------------------------"
if $HAS_SRC; then
  cat << SRCGUIDE
  1. 执行：crash> dis -l <崩溃函数名>
     确认输出的源码行号与 ${SRC_DIR} 中对应文件内容吻合
  2. 若行号吻合 → 版本匹配，继续
     若不吻合 → 标注"版本差异"，继续分析但在结论中说明
  3. 注意编译器优化：
     - 函数内联：源码有调用但汇编里已展开，按汇编逻辑追
     - 变量寄存器化：从寄存器值推断局部变量内容
SRCGUIDE
else
  echo "  获取源码后：执行 dis -l <崩溃函数名>，将 RIP 对应到源码精确行号"
fi

echo ""
echo "【Step S3】调用栈逐帧源码追踪"
echo "------------------------------------------------------------------"
cat << 'SRCGUIDE'
对调用栈每一帧（从 #0 向上）：
  ① 打开对应源码文件，找到 dis -l 给出的行号
  ② 用 vmcore 的寄存器/栈内容验证函数的输入参数
  ③ 判断：崩溃帧(终点) 还是 根因帧(状态异常首次出现)?

根因帧的特征：
  - 该帧的某个输入/返回值是 NULL
  - 但其调用者（下一帧）没有对此做 NULL 检查
  - 这就是"应有检查但缺失"的位置

重点检查：
  □ 帧 #1 是否对帧 #0 函数的返回值做了 NULL 检查？
  □ 哪个函数首次将 NULL 赋给该指针？
  □ 是否有 early return / goto err 路径绕过了初始化？
  □ 是否存在并发置 NULL（TOCTOU 竞态）？
SRCGUIDE

echo ""
echo "【Step S4】数据流溯源"
echo "------------------------------------------------------------------"
if $HAS_SRC; then
  cat << SRCGUIDE
在源码中追踪 NULL 指针的完整生命周期：

  初始化路径：grep -rn "= alloc\|= kmalloc\|= kzalloc\|= get_" ${SRC_DIR}/
  所有赋值：  grep -rn "->ptr\s*=" ${SRC_DIR}/path/to/file.c
  置 NULL：   grep -rn "= NULL" ${SRC_DIR}/path/

特别检查「错误处理路径」（最常见的根因位置）：
  grep -n "return\|goto err\|goto out" ${SRC_DIR}/path/to/崩溃函数所在文件.c
  → 找所有 early return 路径，逐条检查指针在该路径上是否被正确初始化

  git -C ${SRC_DIR} log --oneline --grep="null\|NULL\|fix.*init" -- path/ | head -10
  → 查历史是否有类似修复，帮助快速定位模式
SRCGUIDE
else
  echo "  获取源码后，使用 grep 追踪指针的分配/置NULL/使用全路径"
  echo "  重点检查 goto err / early return 路径上的指针初始化"
fi

echo ""
echo "【Step S5】反事实验证（不允许跳过）"
echo "------------------------------------------------------------------"
cat << 'VERIFY'
假设根因为：<你在源码中找到的代码缺陷>

正向推演：
  ① 该缺陷在什么条件下被触发？
  ② 触发后 NULL 如何传播到崩溃点？（逐帧推演）
  ③ 预期崩溃位置：<推演结果>

交叉比对（与 vmcore 轨道结论对照）：
  推演崩溃位置 == vmcore RIP 位置？  □ 是  □ 否
  推演异常值   == vmcore 寄存器值？  □ 是  □ 否
  推演调用路径 == vmcore bt 路径？   □ 是  □ 否
  → 三条全是：高置信度，进入结论收敛
  → 有不符：检查版本差异或重新确认根因帧
VERIFY

# --------------------------------------------------------------------------
# 源码轨道结论模板
# --------------------------------------------------------------------------
echo ""
echo "=================================================================="
echo " ▶ 源码轨道结论（填写后与 vmcore 轨道交叉验证）"
echo "=================================================================="
cat << 'SRC_CONCLUSION'

  根因代码位置：<file>:<line>  函数：<func>()
  缺陷类型：[缺失NULL检查 | 错误路径遗漏 | 并发竞态 | 其他]
  代码缺陷：<例："setup_device() 在 IRQ 初始化失败时 goto err_irq，
              但未执行 dev->buf = NULL，导致后续 cleanup() 访问野指针">
  触发条件：<需要什么前置状态，如"IRQ 申请失败场景">
  因果链：[缺陷触发] → [NULL未被置位] → [传播] → [bt #0 崩溃]

SRC_CONCLUSION

echo "=================================================================="
echo " ▶ 交叉验证清单（两轨结论填完后执行）"
echo "=================================================================="
cat << 'CROSS_VERIFY'

  崩溃位置吻合：□ 是（RIP 对应 <file>:<line>）  □ 否（差异：<说明>）
  异常值吻合：  □ 是（寄存器 NULL 与源码逻辑一致）□ 否（差异：<说明>）
  调用路径吻合：□ 是（bt 路径与源码调用一致）      □ 否（差异：<说明>）
  根因帧吻合：  □ 是（vmcore #N 与源码缺陷行一致）  □ 否（差异：<说明>）

  → 全部吻合：高置信度，输出最终报告
  → 有不符：优先信任 vmcore 客观数据，重新检查源码假设

## 最终修复建议（交叉验证通过后填写）
  最小修复：
    在 <file>:<line> 添加：
    +  if (unlikely(!ptr)) {
    +      dev_err(..., "unexpected NULL\n");
    +      return -EINVAL;
    +  }
  根本修复：<是否有更深层的生命周期管理问题>

CROSS_VERIFY
