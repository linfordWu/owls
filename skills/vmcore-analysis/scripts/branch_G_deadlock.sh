#!/usr/bin/env bash
# =============================================================================
# 脚本：branch_G_deadlock.sh
# 用途：死锁故障完整分析（源码主导 + 纯vmcore 双路径）
# 使用：bash branch_G_deadlock.sh <vmcore_path> <vmlinux_path> [src_dir]
# =============================================================================

set -euo pipefail

VMCORE="${1:-/var/crash/vmcore}"
VMLINUX="${2:-/usr/lib/debug/lib/modules/$(uname -r)/vmlinux}"
SRC_DIR="${3:-}"
CRASH_CMD="crash -s ${VMLINUX} ${VMCORE}"

if [[ "${1:-}" == "--help" ]] || [[ "${1:-}" == "-h" ]]; then
  echo "用途：死锁（Deadlock）完整分析"
  echo "使用：bash $0 <vmcore> <vmlinux> [src_dir]"
  exit 0
fi

HAS_SRC=false
[[ -n "${SRC_DIR}" && -d "${SRC_DIR}" ]] && HAS_SRC=true

echo "=================================================================="
echo " 分支G：死锁分析"
echo " 分析路径：$( $HAS_SRC && echo '【源码主导】' || echo '【纯vmcore】' )"
echo "=================================================================="

# --------------------------------------------------------------------------
# G1. LOCKDEP 报告（最直接的死锁证据）
# --------------------------------------------------------------------------
echo ""
echo "【G1】LOCKDEP 死锁报告"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
log | grep -B2 -A40 "possible circular locking dependency\|LOCKDEP"
EOF

echo ""
echo "【G1.1】watchdog/hung_task 触发信息"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
log | grep -E "watchdog|hung_task|INFO: task.*blocked|possible deadlock"
EOF

# --------------------------------------------------------------------------
# G2. 所有 CPU 调用栈（核心：找到互相等待锁的任务）
# --------------------------------------------------------------------------
echo ""
echo "【G2】所有 CPU 调用栈（找等待锁的任务）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
bt -a
EOF

# --------------------------------------------------------------------------
# G3. D 状态进程（等待锁而阻塞）
# --------------------------------------------------------------------------
echo ""
echo "【G3】D 状态（UNINTERRUPTIBLE）进程"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
ps | grep " D "
EOF

echo ""
echo "【G3.1】D 状态进程详细调用栈"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
# 对每个 D 状态进程打印详细调用栈
foreach task (ps | grep " D " | awk '{print $1}') { bt -f $task }
EOF

# --------------------------------------------------------------------------
# G4. 锁对象状态（找到 owner 和 waiter 关系）
# --------------------------------------------------------------------------
echo ""
echo "【G4】锁对象状态检查"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
# 从调用栈中识别锁地址（mutex_lock/down_read 等函数的参数）
# 然后执行：struct mutex <addr>  查看 owner 字段
echo "请从 G2/G3 调用栈中提取锁地址（mutex_lock/spin_lock 的参数），执行："
echo "  struct mutex <lock_addr>            # mutex owner 字段"
echo "  struct rw_semaphore <lock_addr>     # 读写锁状态"
echo "  struct spinlock <lock_addr>         # spinlock 状态"
EOF

# --------------------------------------------------------------------------
# G5. 运行队列状态
# --------------------------------------------------------------------------
echo ""
echo "【G5】运行队列（确认任务调度状态）"
echo "------------------------------------------------------------------"
${CRASH_CMD} --no_scroll << 'EOF'
runq
EOF

# --------------------------------------------------------------------------
# G6. 源码路径：锁依赖图构建
# --------------------------------------------------------------------------
if $HAS_SRC; then
  echo ""
  echo "【G6】[源码路径] 锁依赖图构建指引"
  echo "------------------------------------------------------------------"
  cat << SRCGUIDE

Step A：从 G2/G3 确认死锁参与者
  记录：任务A（PID=<>）等待 <锁X>，持有 <锁Y>
       任务B（PID=<>）等待 <锁Y>，持有 <锁X>

Step B：在源码中找到两把锁的定义
  grep -rn "mutex\|spinlock\|rwsem\|semaphore" ${SRC_DIR}/path/ | grep "<锁名称>"

Step C：全局搜索同时获取这两把锁的所有代码路径
  grep -rn "mutex_lock.*lockA\|mutex_lock.*lockB\|spin_lock.*lockA" ${SRC_DIR}/
  → 对每条路径，记录加锁顺序（A→B 还是 B→A）

Step D：找到加锁顺序相反的路径
  路径1：先锁A，后锁B  （某个函数）
  路径2：先锁B，后锁A  ← 这是引入死锁的路径，这里是根因帧

Step E：反事实验证（Step 5）
  如果路径2也改成先锁A后锁B（统一顺序），死锁是否消除？
  验证：修改后是否还存在环形等待？

【源码搜索命令】
  # 找所有加锁操作
  grep -rn "mutex_lock\|spin_lock\|down_read\|down_write" ${SRC_DIR}/path/file.c

  # 找特定锁变量的所有使用
  grep -rn "<lock_variable_name>" ${SRC_DIR}/path/

  # git 历史查找死锁相关修复
  git -C ${SRC_DIR} log --oneline --grep="deadlock\|locking order\|circular" | head -10

SRCGUIDE
else
  echo ""
  echo "【G6】[vmcore路径] 锁依赖图构建"
  echo "------------------------------------------------------------------"
  ${CRASH_CMD} --no_scroll << 'EOF'
# 通过任务状态和锁 owner 构建依赖图
# 步骤：
# 1. 从 G3.1 找到等待锁的 mutex_lock/spin_lock 调用
# 2. 提取函数参数（锁地址）
# 3. struct mutex <addr> 查 owner
# 4. 如 owner 也在等待另一把锁，即发现环形等待
log | grep -E "lock_class|lock chain|held lock" | head -30
EOF
fi

# --------------------------------------------------------------------------
# G7. 反事实验证（Step 5）
# --------------------------------------------------------------------------
echo ""
echo "=================================================================="
echo " Step 5：反事实验证清单"
echo "=================================================================="
cat << 'VERIFY'

【验证1】环形等待链完整性
  绘制锁依赖图：
    任务A --等待--> 锁X <--持有-- 任务B --等待--> 锁Y <--持有-- 任务A
  该环是否自洽？（每一步都能在 vmcore 中验证）  □ 是  □ 否

【验证2】根因路径识别
  加锁顺序不一致的路径是哪一条？（对比其他路径的加锁顺序）
  该路径最近是否有代码变更？（git log 检查）

【验证3】修复有效性
  修复方案：统一加锁顺序为 <锁A> → <锁B>
  修复后是否还有其他路径可能形成新的环？  □ 否（验证通过）  □ 是（需继续排查）

VERIFY

echo ""
echo "=================================================================="
echo " 分析结论模板"
echo "=================================================================="
cat << 'TEMPLATE'

## 崩溃概要
  故障模式：死锁（Deadlock）
  置信度：<高/中/低>
  分析路径：<源码主导 / 纯vmcore>

## 锁依赖图（死锁环）
  任务A (PID=<>) --等待--> 锁X (0x<addr>) <--持有-- 任务B (PID=<>)
  任务B (PID=<>) --等待--> 锁Y (0x<addr>) <--持有-- 任务A (PID=<>)

## 根因结论
  加锁顺序不一致：
    路径1（<函数名>）：先获取 <锁A>，再获取 <锁B>
    路径2（<函数名>）：先获取 <锁B>，再获取 <锁A>  ← 根因路径
  根因代码位置：<file>:<line>  函数：<func>

## 修复建议
  最小修复：在 <file>:<line> 调整加锁顺序，统一为先锁<A>后锁<B>
  根本修复：使用 LOCKDEP 注解静态检测所有路径的锁顺序一致性

TEMPLATE
